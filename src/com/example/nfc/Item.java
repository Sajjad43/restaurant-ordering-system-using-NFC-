package com.example.nfc;

public class Item {

	 String name="";
	 String price="";
	 int count=0; 
	 
	 
	 
	 public Item(String name,String price)
	 {
		 
		 

		 this.price=price;
		 this.name=name;
	 }
	
	
	
	
	




	public int getCount() {
		return count;
	}









	public void setCount(int count) {
		this.count = count;
	}









	public String getPrice() {
		return price;
	}









	public void setPrice(String price) {
		this.price = price;
	}









	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}









	 

	 }
